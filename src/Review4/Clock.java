package Review4;

public class Clock {
    public static void main(String[] args) {

        for (int i = 0; i <= 1; i++) {
            for (int b = 0; b <= 0; b++) {
                for (int c = 1; c <= 59; c++) {

                    System.out.println(i + " " + b + " " + c);
                }
            }
        }
    }
}