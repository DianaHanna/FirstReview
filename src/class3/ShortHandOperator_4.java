package class3;

public class ShortHandOperator_4 {
    public static void main(String[] args) {
        int number=10;
        //number=number+10; is same as writing number+=10
number*=20; // whatever value is stored in number variable add v10 to it
        System.out.println(number);

        int number2=20;
        number2-=10;
        System.out.println(number2);

        int number3=100;
        number3/=10;
        System.out.println(number3);
    }
}
