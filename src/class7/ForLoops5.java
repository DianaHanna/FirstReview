package class7;

public class ForLoops5 {
    public static void main(String[] args) {

        for (int i = 1; i < 10; i++) {
            System.out.println("5* " + i + " = " + (5 * i));
        }
        for (int i = 1; i < 10; i++) {
            System.out.println("3* " + i + " = " + (3 * i));
        }
    }
}