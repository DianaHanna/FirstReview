package class7;

public class ForLoops3 {
    public static void main(String[] args) {

        /*
        when you know exactly how many times you want to loop
        through a block of code, use FOR loop instead of WHILE loop
        when we don't know how many times we need to repeat a block
        of code we should be going with WHILE loop, most of the time it happens
        when user input involved
         */


        for (int i = 0;i<10;i++){
            System.out.println(i);
        }


        /*
        int i = 0;
        i<10;
        System.out.println(i);
        i++
        i < 10;
        System.out.println(i);
         */
    }
}
